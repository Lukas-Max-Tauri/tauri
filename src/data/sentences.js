// Helper function to get audio URL for a language
export const getAudioUrl = (text, language) => {
  const languageMapping = {
    // Direct mappings
    english: 'en-GB',
    spanish: 'es-ES',
    turkish: 'tr-TR',
    arabic: 'ar-XA',
    ukrainian: 'uk-UA',
    russian: 'ru-RU',
    polish: 'pl-PL',
    romanian: 'ro-RO',
    italian: 'it-IT',
    
    // Fallback mappings
    ku: 'tr-TR',      // Kurdish -> Turkish
    farsi: 'ar-XA',   // Farsi -> Arabic
    albanian: 'it-IT', // Albanian -> Italian
    serbian: 'ru-RU',  // Serbian -> Russian
    pashto: 'ar-XA',  // Pashto -> Arabic
    somali: 'ar-XA',  // Somali -> Arabic
    tigrinya: 'ar-XA' // Tigrinya -> Arabic
  };

  return `/tts/${languageMapping[language]}/${encodeURIComponent(text)}`;
};

// Function to generate audio for a sentence
export async function generateAudio(text, language) {
  try {
    const SERVER_URL = import.meta.env.VITE_API_URL || 'http://localhost:5001';
    const response = await fetch(`${SERVER_URL}/tts`, {
      headers: {
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        text,
        language: getLanguageCode(language)
      }),
    });
    
    if (!response.ok) {
      throw new Error(`HTTP error! status: ${response.status}`);
    }
    
    const data = await response.json();
    return data.audioUrl;
  } catch (error) {
    console.error('Error generating audio:', error);
    return null;
  }
}

// Helper function to get the correct language code
function getLanguageCode(language) {
  const languageCodes = {
    english: 'en-GB',
    spanish: 'es-ES',
    turkish: 'tr-TR',
    arabic: 'ar-XA',
    ukrainian: 'uk-UA',
    russian: 'ru-RU',
    polish: 'pl-PL',
    romanian: 'ro-RO',
    italian: 'it-IT',
    ku: 'tr-TR',
    farsi: 'ar-XA',
    albanian: 'it-IT',
    serbian: 'ru-RU',
    pashto: 'ar-XA',
    somali: 'ar-XA',
    tigrinya: 'ar-XA'
  };
  
  return languageCodes[language] || 'de-DE';
}